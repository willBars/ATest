package com.itpxw.utilstest;

import android.content.Context;
import android.widget.Toast;

/**
 * Created by pxw on 2020/10/31.
 */
public class ToastUtils {

    public static void showToast(Context context,String msg){
        Toast.makeText(context,msg,Toast.LENGTH_LONG).show();
    }

    public static void showToastNew(Context context,String msg){
        Toast.makeText(context,msg,Toast.LENGTH_LONG).show();
    }


    public static void showToastNewNew(Context context,String msg){
        Toast.makeText(context,msg,Toast.LENGTH_LONG).show();
    }

    public static void showToastNewNewNew(Context context,String msg){
        Toast.makeText(context,msg,Toast.LENGTH_LONG).show();
    }
}
